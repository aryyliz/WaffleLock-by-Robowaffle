void checkID(){
	if(HAL_I2C_Mem_Read(&hi2c1, BMA400ADDRESS,0x00, 1, pData_ID, 1, 100) == HAL_OK){
		uint8_t valID = pData_ID[0];
	    if (valID == CHIPID){
			HAL_GPIO_WritePin(LED_VERDE_GPIO_Port, LED_VERDE_Pin, 1);
			HAL_Delay(3000);
			HAL_GPIO_WritePin(LED_VERDE_GPIO_Port, LED_VERDE_Pin, 0);
		}
	}
	else{
		Error_Handler();
	}
}

void goToNormal(){
	uint8_t pData = 0x02;
	if(HAL_I2C_Mem_Write(&hi2c1, BMA400ADDRESS,0x19, 1, &pData, 1, 100) == HAL_OK){
		HAL_GPIO_WritePin(LED_VERDE_GPIO_Port, LED_VERDE_Pin, LEDON);
		HAL_Delay(3000);
		HAL_GPIO_WritePin(LED_VERDE_GPIO_Port, LED_VERDE_Pin, LEDOFF);
	}
}
void startTimer6(){
	HAL_TIM_Base_Start_IT(&htim6);
}

void readTempBMA400(){
	uint8_t pData_Temp[1];
	HAL_I2C_Mem_Read(&hi2c1, BMA400ADDRESS, 0x11, 1, pData_Temp, 1, 100);
	int8_t temperatureRaw = pData_Temp[0];
	temperature = temperatureRaw * 0.5f + 23.0f;
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
  if (htim->Instance == TIM6) {
	  readTempBMA400();
	  readGasConcentrationMQ();
	  checkForFire();
  }
}

